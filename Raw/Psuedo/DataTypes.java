public class DataTypes {
    public static void main(String[] args) {
        byte b = 127;
        System.out.println("The value of byte B is : "+ b); 

        char ch = 'a';
        System.out.println("The value of Character ch is: "+ ch);

        boolean bool = true;
        System.out.println("The value of boolean is: "+ bool);

        float price = 10.5f;
        System.out.println("The value of float PI is: "+price);


    }
    
}
